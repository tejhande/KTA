package default_1;

public class Data_Types {

	public static void main(String[] args) {
//		Primitive DataTypes
		byte byte_var = 12; //variable of byte 
		System.out.println("The value byte is "+byte_var);

		short short_var = 10; // variable of short 
		System.out.println("The value of short is "+short_var);

		int int_var = 100; // variable of int 
		System.out.println("The value of int is "+int_var);

		float float_var = 10.50f; // variable of float 
		System.out.println("The value of float is "+float_var);

		long long_var = 454875l; // variable of long 
		System.out.println("The value of long is "+long_var);

		double double_var = 244.456; // variable of double 
		System.out.println("The value of double is "+double_var);

		char char_var = 'd';// variable of char 
		System.out.println("The value of char is "+char_var);

		boolean boolean_var = false; // variable of boolean 
		System.out.println("The value of boolean is "+boolean_var);
		
		
//		Non Primitive DataTypes		
		
		String name = "AB54545C";
		System.out.println("The value of String is "+name);
			
				
			}
	
}