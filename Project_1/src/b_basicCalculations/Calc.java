package default_1;

public class Calc {

	public static void main(String[] args) {
		 // Declare variables
        int a = 52;
        int b = 6;

        // Perform operations
        int sum = a + b;
        int difference = a - b;
        int product = a * b;
        int quotient = a / b; // Note: This performs integer division

        // Print results
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + difference);
        System.out.println("Product: " + product);
        System.out.println("Quotient: " + quotient);

	}

}
