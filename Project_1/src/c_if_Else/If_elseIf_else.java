package default_1;

public class If_elseIf_else {

	public static void main(String[] args) {

		int a = 55;
		if (a < 0) {
			System.out.println("A is less Than Zero");

		} else if (a < 50) {
			System.out.println("A is less Than Fifty");

		} else {
			System.out.println("A is Greater Than Fifty");

		}

	}

}
